//****************************************************************
void solve()
{
    int n, k;
    cin >> n >> k;
    vector<int> nums(n);
    for (int i = 0; i < n; i++)
        cin >> nums[i];
    int ans = 0;
    int count = 1;

    for (int i = 1; i < n; i++)
    {
        if (2 * nums[i] > nums[i - 1])
            count++;
        else
        {
            ans += max(0, count - k);
            count = 1;
        }
    }
    ans += max(0, count - k);
    cout << ans;
    br;
}

int main()
{
    int t;
    cin >> t;
    for (int i = 1; i <= t; i++)
    {
        solve();
    }

    return 0;
}